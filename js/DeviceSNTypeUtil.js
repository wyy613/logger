    getElement(deviceType, standard, brand, brandDetail) {
        // console.log('params:', deviceType, standard, brand, brandDetail);
        // let keys = Object.keys(this)
        const currentItem = Object.keys(this).find(
            key =>
                this[key].bitVals[0] === deviceType &&
                this[key].bitVals[1] === standard &&
                this[key].bitVals[2] === brand &&
                this[key].bitVals[3] === brandDetail
        );
        let curItem = null;
        if (currentItem) {
            curItem = this[currentItem];
        }
        const item = Object.keys(this).find(
            key => this[key].bitVals[0] === deviceType && this[key].bitVals[1] === standard
        );
        if (item) {
            if (curItem) {
                curItem.parentBrand = this[item].subBrand;
            } else {
                curItem = {
                    brand: '--',
                    subBrand: '--',
                    type: this[item].type,
                    parentBrand: this[item].subBrand,
                    subBrand:this[item].subBrand
                };
            }
            return curItem;
        } else {
            return {
                brand: 'SN ERROR！',
                subBrand: 'SN ERROR！',
                type: 'SN ERROR！',
                parentBrand: 'SN ERROR！'
            };
        }
    }
